#include <stdio.h>
#include "header.h"
/**
 * add - adds two integers
 * @a: first int
 * @b: second int
 *
 * Return: value after addition
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: value after subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first int
 * @b: second int
 *
 * Return: value after multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two integers
 * @a: first int
 * @b: second int
 *
 * Return: value after division
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - modulus of a value
 * @a: first int
 * @b: second int
 *
 * Return: modulus of the integers
 */
int mod(int a, int b)
{
	return (a % b);
}
